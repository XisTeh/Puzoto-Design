/* ─── I18N ENGINE ─── */
(function () {
    'use strict';

    const STORAGE_KEY = 'puzoto-lang';
    const DEFAULT_LANG = 'pt';

    function getSavedLang() {
        return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG;
    }

    function saveLang(lang) {
        localStorage.setItem(STORAGE_KEY, lang);
    }

    function applyTranslations(lang) {
        const dict = translations[lang];
        if (!dict) return;

        // Update <html lang>
        document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';

        // Update <title> and <meta description>
        document.title = dict['meta-title'];
        const metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) metaDesc.setAttribute('content', dict['meta-desc']);

        // Update all [data-i18n] elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key] !== undefined) {
                // Check if translation contains HTML (like <span>)
                if (dict[key].includes('<')) {
                    el.innerHTML = dict[key];
                } else {
                    el.textContent = dict[key];
                }
            }
        });

        // Update active states on language buttons
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('is-active', btn.getAttribute('data-lang') === lang);
        });

        // Update marker labels array for team logic
        if (window._updateMarkerLabels) {
            window._updateMarkerLabels(lang);
        }
    }

    function initI18n() {
        const lang = getSavedLang();

        // Bind language switch buttons
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                const newLang = btn.getAttribute('data-lang');
                if (newLang === getSavedLang()) return;
                saveLang(newLang);
                applyTranslations(newLang);
            });
        });

        // Apply initial language
        applyTranslations(lang);
    }

    // Expose for team logic update
    window._updateMarkerLabels = function (lang) {
        const labels = lang === 'en'
            ? ['Founder', 'Management']
            : ['Fundador', 'Gestão'];
        // Update the global markerLabels if accessible
        if (window._markerLabelsRef) {
            window._markerLabelsRef.length = 0;
            window._markerLabelsRef.push(...labels);
        }
        // Also immediately update the visible marker
        const marker = document.querySelector('#teamMarkerText');
        const activeIdx = [...document.querySelectorAll('.team__thumb')].findIndex(t => t.classList.contains('is-active'));
        if (marker && labels[activeIdx] !== undefined) {
            marker.textContent = labels[activeIdx];
        }
    };

    // Run on DOM ready
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initI18n);
    } else {
        initI18n();
    }
})();
